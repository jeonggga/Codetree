# 첫 번째 입력: 리스트의 원소 개수
n1 = int(input())  
# 예: 입력 3 → n1 = 3

# 두 번째 입력: 공백으로 구분된 정수들을 리스트로 변환
n2 = list(map(int, input().split()))  
# 예: 입력 "1 8 5" → n2 = [1, 8, 5]

# 리스트 n2의 각 원소를 제곱하여 새로운 리스트 a_list에 저장
a_list = [a*a for a in n2]  
# 결과: [1, 64, 25]

# 리스트 a_list의 각 원소를 문자열로 변환
a_list_str = list(map(str, a_list))  
# 결과: ['1', '64', '25']

# 리스트 문자열을 공백으로 연결하여 하나의 문자열로 만듦
b = " ".join(a_list_str)  
# 결과: "1 64 25"

# 최종 결과 출력
print(b)  # 출력: 1 64 25